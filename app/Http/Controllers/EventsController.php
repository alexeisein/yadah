    {
        $this->validate($request,[
          'title' => 'required|min:3|max:255',
          'description' => 'max:225',
          'artists' => 'max:255',
          'location' => 'max:255',
          'ticket' =>'max:11',
          'host' => 'max:225',
          'date' => 'max:150',
          'slug' => 'required|unique:posts,slug|alpha_dash|max:100',
          'event_image' => 'mimes:jpeg,png,jpg|max:1999',
        ]);
    }

